package es.reaktor.models.horarios;

public class Grupo
{
	private String numIntGr;
	private String abreviatura;
	private String nombre;

	public String getNumIntGr()
	{
		return numIntGr;
	}

	public void setNumIntGr(String value)
	{
		this.numIntGr = value;
	}

	public String getAbreviatura()
	{
		return abreviatura;
	}

	public void setAbreviatura(String value)
	{
		this.abreviatura = value;
	}

	public String getNombre()
	{
		return nombre;
	}

	public void setNombre(String value)
	{
		this.nombre = value;
	}
}