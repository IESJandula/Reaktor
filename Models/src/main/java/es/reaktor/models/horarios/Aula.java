package es.reaktor.models.horarios;

public class Aula
{
	private String numIntAu;
	private String abreviatura;
	private String nombre;

	public String getNumIntAu()
	{
		return numIntAu;
	}

	public void setNumIntAu(String value)
	{
		this.numIntAu = value;
	}

	public String getAbreviatura()
	{
		return abreviatura;
	}

	public void setAbreviatura(String value)
	{
		this.abreviatura = value;
	}

	public String getNombre()
	{
		return nombre;
	}

	public void setNombre(String value)
	{
		this.nombre = value;
	}
}
